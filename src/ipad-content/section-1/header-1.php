		<p>Plannotate3 &#40;&quot;P3&quot;, &quot;Plannotate&quot;&#41; is the successor to Plannotate2 &#40;&quot;P2&quot;&#41;, a software platform for the collection of field investigation and condition assessment data. Plannotate is a SaaS application for the Apple iPad iOS and computer web browser that interfaces with an internet accessible web server, otherwise known as the Plannotate Cloud. </p>
				
