	<h2 class="section-heading">1.1 Installing Plannotate iPad Application</h2>
	<p>Plannotate can be installed as a native iOS application on any Apple supported iPad. In order to install the application, follow these steps on your iPad.</p>
	
	<figure class="figure docs-figure py-3">
		<a href="ipad-content/section-1/ipad-install.gif" data-title="Installing Plannotate on an iPad" data-toggle="lightbox"><img class="figure-img img-fluid shadow rounded" src="ipad-content/section-1/ipad-install.gif" loading="lazy" alt="" style="width: 600px;"></a>
		<figcaption class="figure-caption mt-3">Installing Plannotate from an iPad&apos;s Safari Browser.</figcaption>
	</figure>
	
	<ol>
		<li>Open the <b>Safari browser</b> on your iPad.</li>
		<li>Navigate to the <a class="theme-link" href="http://54.161.33.218/" target="_blank"><i class="fas fa-external-link-alt"></i> Plannotate 3 page</a>.</li>
		<li>Select the <b>Apple icon</b> in the top right corner.</li>		
		<li>Navigate to your iPad's <b>Settings &#62; General &#62; Device Management</b>.</li>
		<li>Select <b>Wiss, Janney, Elstner Associates, Inc.</b></li>
		<li>Select <b>Trust &quot;Wiss, Janey, Elstner Associates, Inc.&quot;</b></li>
		<li>Select <b>Trust</b>.</li>
		
		<li>Select the <b>Plannotate icon</b> on your iPad&apos;s Home Screen to launch the application.</li>
		
