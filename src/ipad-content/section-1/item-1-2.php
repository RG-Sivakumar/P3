<section class="docs-section" id="item-1-2">
	<h2 class="section-heading">1.2 Signing In To Plannotate</h2>
	<p>Once Plannotate is launched, you will be presented with a sign in screen where you can provide your login credentials.</p>
	
	<div class="callout-block callout-block-info">
                            
		<div class="content">
			<h4 class="callout-title">
				<span class="callout-icon-holder mr-1">
					<i class="fas fa-info-circle"></i>
				</span><!--//icon-holder-->
				Note
			</h4>
			<ul>
				<li>For WJE employees, your Plannotate login credentials will be the same as your computer login credentials. For example, your email will be your username@wje.com email address and your password will be your network password.</li>
				<li>For external users, you will enter the email address and password that has been provided to you when added as a user to the product.</li>
			</ul>
		</div><!--//content-->
	</div><!--//callout-block-->
	
	<figure class="figure docs-figure py-3">
		<a href="ipad-content/section-1/login.gif" data-title="Signing In To Plannotate" data-toggle="lightbox"><img class="figure-img img-fluid shadow rounded" src="ipad-content/section-1/login.gif" loading="lazy" alt="" style="width: 600px;"></a>
		<figcaption class="figure-caption mt-3">Signing In To Plannotate</figcaption>
	</figure>
	
	<ol>
		<li><b>Email Address</b> – Your WJE email or guest email address.</li>
		<li><b>Password</b> – Your WJE network password or guest password. By selecting the icon to the right of the password text box, you can toggle viewing or hiding the password as you enter it.</li>
		<li><b>Remember me?</b> – By selecting this option, Plannotate will remember your email address for faster access after signing out.</li>
		<li><b>Forgot password</b> – This option will apply to external users that have been granted access to Plannotate.  For WJE employees, if you cannot recall your network password, you will need to follow the IT policies and procedures to reset your password.</li>
		<li><b>Sign In</b> – Once your login credentials have been entered, select Sign In to enter the product.</li>
	</ol>
