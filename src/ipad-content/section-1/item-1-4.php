	<h2 class="section-heading">1.4 Popup Notifications</h2>
	<p>Upon logging into Plannotate for the first time, you will be shown several popup windows requesting access to specific iPad functionality.</p>
	
	<ul>
		<li>Plannotate3 requests access to your camera in order to take photos.</li>
		<li>Plannotate3 requests access to your photos in order to access your camera roll</li>
	</ul>
	
	<div class="callout-block callout-block-warning">
		<div class="content">
			<h4 class="callout-title">
				<span class="callout-icon-holder mr-1">
					<i class="fas fa-bullhorn"></i>
				</span><!--//icon-holder-->
				Warning
			</h4>
			<p>It is important that you hit <b>OK</b> to grant access to your device&#39;s camera and <b>Allow Access to All Photos</b> or <b>Select Photos</b> in order to utilize the media functions within Plannotate.</p>
		</div><!--//content-->
	</div><!--//callout-block-->
	
